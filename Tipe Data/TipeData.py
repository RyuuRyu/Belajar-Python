### Beberapa Tipe Data di Python

# Tipe Data String
Text = "This is a string"
print(Text, "\n")

# Tipe Data Integer
number = 123
print(number, "\n")

# Tipe Data Boolean
bool = True or False
print(bool, "\n")

# Tipe Data Float
pi = 3.14
print(pi, "\n")

# Tipe Data List (Array)
fruits = ["Apple", "Banana", "Cherry"]
print(fruits, "\n")

# Tipe Data Tuple (Array yang tidak bisa diubah)
koordinat = (123, 456)
print(koordinat, "\n")

# Tipe Data Dictionary (Array yang memiliki key dan value)
person = {
    "name": "Ryu",
    "age": 20,
    "address": "Jakarta"
}
print(person, "\n")

# Tipe Data Set
set = {"Apple", "Banana", "Cherry"}
print(set, "\n")

# Tipe Data Frozen Set
frozenSet = frozenset({"Apple", "Banana", "Cherry"})
print(frozenSet, "\n")