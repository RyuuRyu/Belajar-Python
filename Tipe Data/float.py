### Tipe Data Float
a = 10.5
b = 5.5
print(a, b)

## Tipe data float adalah tipe data yang berisi bilangan desimal.
## Berbeda dengan integer, float ditulis dengan menggunakan tanda titik (.)
## Float adalah singkatan dari "floating point".

### Contoh 1
print(a * b)
print("\n")

## Tipe data float biasa digunakan untuk operasi matematika yang membutuhkan bilangan desimal.
## Dan mendapatkan hasil operasi aritmatika yang lebih akurat.
## Seperti pembagian 10 / 3 = 3.3333333333333335

### Conoth 2

alas = 10
tinggi = 25

def luas_segitiga(alas, tinggi):
    return 1/2 * alas * tinggi

print("Luas segitiga dengan alas 10cm, dan tinggi 25cm adalah ", luas_segitiga(alas, tinggi), "cm\u00b2")